    public void categoriasBD() {
        CategoriasRepository cateRepository = new CategoriasRepository();
        this.categorias = cateRepository.getAllCategorias();
        for (int i = 0; i < categorias.size(); i++) {
            Categorias categoria = categorias.get(i);
            combCategoria.addItem(categoria.getCat_nombre());
        }        
    }
    
    public void limitadorSpi() {
        SpinnerNumberModel nm = new SpinnerNumberModel();
        nm.setMinimum(0);
        spiStock.setModel(nm);
    }
    
    public void setProducto(String referencia, String categoria) {
        ProductosRepository proRepository = new ProductosRepository(); 
        String sqlQuery = proRepository.eleccionSentencia("referencia");
        Productos producto = proRepository.getProducto(referencia, sqlQuery).get(0);
        
        idProducto = producto.getPro_id();
        txtReferencia.setText(producto.getPro_referencia());
        txtNombre.setText(producto.getPro_nombre());
        spiStock.setValue(producto.getPro_stock());
        txtPrecio.setText(Integer.toString(producto.getPro_precio()));
        combCategoria.setSelectedItem(categoria);

    }

